"""
═══════════════════════════════════════════════════════════
ANTIGRAVITY OS v3 — Ragas RAG Quality Evaluator (§39)
═══════════════════════════════════════════════════════════

Automated RAG quality monitoring without human labels.
Runs daily at 4am to detect quality drift before visitors notice.

Metrics:
  faithfulness:       Is the answer grounded in retrieved chunks?
  answer_relevancy:   Is the answer relevant to the question?
  context_precision:  Are the retrieved chunks actually useful?
  context_recall:     Are the right chunks being retrieved?
"""

from __future__ import annotations

import logging
from dataclasses import dataclass
from datetime import datetime
from typing import Any

logger = logging.getLogger("portfolio.evaluation.ragas")


@dataclass
class RAGEvalResult:
    """Results from a Ragas evaluation run."""
    faithfulness: float = 0.0
    context_precision: float = 0.0
    context_recall: float = 0.0
    answer_relevancy: float = 0.0
    dataset_size: int = 0
    evaluation_date: datetime | None = None
    alerts: list[str] | None = None

    def to_dict(self) -> dict[str, Any]:
        return {
            "faithfulness": self.faithfulness,
            "context_precision": self.context_precision,
            "context_recall": self.context_recall,
            "answer_relevancy": self.answer_relevancy,
            "dataset_size": self.dataset_size,
            "evaluation_date": (
                self.evaluation_date.isoformat()
                if self.evaluation_date
                else None
            ),
            "alerts": self.alerts or [],
        }


class RAGQualityMonitor:
    """
    Runs continuously to detect RAG degradation before visitors notice.

    Evaluation pipeline:
      1. Load synthetic QA dataset (generated nightly by Ollama)
      2. Run each QA pair through the full RAG pipeline
      3. Compute Ragas metrics
      4. Store in DuckDB for trend analysis
      5. Alert via ntfy if any metric below threshold

    Graceful degradation: if Ragas is not installed or evaluation
    fails, log the error and continue — never crash the system.
    """

    def __init__(self):
        self._ragas_available = False
        self._thresholds = {
            "faithfulness": 0.80,
            "context_precision": 0.75,
            "answer_relevancy": 0.80,
        }

        try:
            import ragas
            self._ragas_available = True
        except ImportError:
            logger.info("⚠️ Ragas not installed — RAG evaluation disabled")

    def configure(
        self,
        faithfulness_threshold: float = 0.80,
        context_precision_threshold: float = 0.75,
        answer_relevancy_threshold: float = 0.80,
    ) -> None:
        """Configure quality thresholds."""
        self._thresholds = {
            "faithfulness": faithfulness_threshold,
            "context_precision": context_precision_threshold,
            "answer_relevancy": answer_relevancy_threshold,
        }

    async def evaluate(
        self,
        eval_dataset: list[dict[str, Any]],
    ) -> RAGEvalResult:
        """
        Run Ragas evaluation on a dataset.

        Each item in eval_dataset should have:
          - question: str
          - answer: str (generated by RAG pipeline)
          - contexts: list[str] (retrieved chunks)
          - ground_truth: str (expected answer)

        Returns RAGEvalResult with all metrics and any alerts.
        """
        if not self._ragas_available:
            logger.warning("Ragas not available, returning empty result")
            return RAGEvalResult()

        if not eval_dataset:
            logger.warning("Empty evaluation dataset")
            return RAGEvalResult()

        try:
            from ragas import evaluate as ragas_evaluate
            from ragas.metrics import (
                faithfulness,
                answer_relevancy,
                context_precision,
                context_recall,
            )
            from datasets import Dataset

            # Build Hugging Face Dataset
            dataset = Dataset.from_list(eval_dataset)

            # Run Ragas evaluation
            scores = ragas_evaluate(
                dataset,
                metrics=[
                    faithfulness,
                    answer_relevancy,
                    context_precision,
                    context_recall,
                ],
            )

            result = RAGEvalResult(
                faithfulness=scores.get("faithfulness", 0.0),
                context_precision=scores.get("context_precision", 0.0),
                context_recall=scores.get("context_recall", 0.0),
                answer_relevancy=scores.get("answer_relevancy", 0.0),
                dataset_size=len(eval_dataset),
                evaluation_date=datetime.utcnow(),
                alerts=[],
            )

            # Check thresholds
            result.alerts = self._check_thresholds(result)

            logger.info(
                f"RAG evaluation complete: "
                f"faithfulness={result.faithfulness:.2f}, "
                f"precision={result.context_precision:.2f}, "
                f"relevancy={result.answer_relevancy:.2f}"
            )

            return result

        except Exception as e:
            logger.error(f"Ragas evaluation failed: {e}")
            return RAGEvalResult()

    def _check_thresholds(self, result: RAGEvalResult) -> list[str]:
        """Check if any metric falls below threshold."""
        alerts = []

        checks = [
            ("faithfulness", result.faithfulness),
            ("context_precision", result.context_precision),
            ("answer_relevancy", result.answer_relevancy),
        ]

        for metric_name, value in checks:
            threshold = self._thresholds.get(metric_name, 0.0)
            if value < threshold:
                alerts.append(
                    f"{metric_name}: {value:.2f} < {threshold:.2f} (DEGRADED)"
                )

        return alerts

    async def generate_synthetic_qa(
        self,
        knowledge_chunks: list[str],
        ollama_url: str = "http://localhost:11434",
        model: str = "qwen2.5:3b",
        num_qa_per_chunk: int = 2,
    ) -> list[dict[str, Any]]:
        """
        Generate synthetic QA pairs from knowledge chunks using Ollama.
        Used to build the evaluation dataset without human labeling.
        """
        import httpx

        eval_dataset: list[dict[str, Any]] = []

        async with httpx.AsyncClient(timeout=60.0) as client:
            for chunk in knowledge_chunks[:50]:  # Limit to 50 chunks
                prompt = (
                    "Given this text from a software engineer's portfolio, "
                    "generate a question that a visitor might ask and the "
                    "correct answer based ONLY on the text.\n\n"
                    f"Text: {chunk[:1000]}\n\n"
                    "Format:\n"
                    "Q: [question]\n"
                    "A: [answer]\n\n"
                    "Generate one Q&A pair."
                )

                try:
                    response = await client.post(
                        f"{ollama_url}/api/generate",
                        json={
                            "model": model,
                            "prompt": prompt,
                            "stream": False,
                            "options": {"temperature": 0.3},
                        },
                    )
                    response.raise_for_status()
                    text = response.json().get("response", "")

                    # Parse Q&A
                    qa = self._parse_qa(text, chunk)
                    if qa:
                        eval_dataset.append(qa)

                except Exception as e:
                    logger.debug(f"QA generation failed for chunk: {e}")
                    continue

        logger.info(f"Generated {len(eval_dataset)} synthetic QA pairs")
        return eval_dataset

    def _parse_qa(
        self, text: str, source_chunk: str
    ) -> dict[str, Any] | None:
        """Parse a Q&A pair from LLM output."""
        lines = text.strip().split("\n")
        question = ""
        answer = ""

        for line in lines:
            stripped = line.strip()
            if stripped.startswith("Q:"):
                question = stripped[2:].strip()
            elif stripped.startswith("A:"):
                answer = stripped[2:].strip()

        if question and answer:
            return {
                "question": question,
                "answer": "",  # Will be filled by RAG pipeline
                "contexts": [source_chunk],
                "ground_truth": answer,
            }
        return None

    @property
    def is_available(self) -> bool:
        return self._ragas_available


# Module-level singleton
rag_evaluator = RAGQualityMonitor()
